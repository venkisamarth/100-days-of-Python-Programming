# range() function
# range() function return an immutable sequnce of members between 
# the given start and stop int 

# syntax (start ,stop,step)
# start =starting position starting integer of range sequance
# stop =ending position .sequnce  and end at stop 
# step increment between each integer in sequnce

print(list(range(0,10,1)))
print(list(range(0,10,2)))
print(list(range(5,1)))
print(list(range(5,1)))
print(list(range(5)))
print(list(range(10,0,-2)))

print(list(range(100,120,5)))
